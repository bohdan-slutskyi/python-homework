""" 02 Количество символов
Напишите программу, которая
- подсчитывает количество вхождений всех символов в строке.

Нужно вывести только символы, которые встречаются более 1 раза
(игнорируя регистр), с указанием их количества.
Выводите повторяющиеся символы только один раз.

text = "Programming in python"

Пример вывода:
Строка: Programming in python
Символ 'p' встречается 1раз(а)
Символ 'r' встречается 2раз(а)
Символ 'o' встречается 2раз(а)
Символ 'g' встречается 2раз(а)
Символ 'a' встречается 1раз(а)
Символ 'm' встречается 2раз(а)
Символ 'i' встречается 2раз(а)
Символ 'n' встречается 3раз(а)
Символ ' ' встречается 2раз(а)
Символ 'y' встречается 1раз(а)
Символ 't' встречается 1раз(а)
Символ 'h' встречается 1раз(а)
"""

# Примечание:
# В условии задачи указано игнорировать регистр,
# поэтому символы 'P' и 'p' считаются одинаковыми.
# В строке "Programming in python" символ 'p' встречается 2 раза,
# однако в примере вывода указано 1 раз.
# Также в примере выводятся символы с 1 вхождением,
# хотя по условию нужно выводить только символы с количеством > 1.
# В данном решении реализована логика строго по условию задачи.
text = "Programming in python"
print("Строка:", text)


# приводим к нижнему регистру (чтобы 'P' и 'p' считались одинаковыми)
text_lower = text.lower()

used = ""  # сюда будем записывать уже обработанные символы (чтобы не обрабатывать повторно)

for char in text_lower:
    # если символ уже обрабатывали — пропускаем его
    if char in used:
        continue

    # считаем количество вхождений символа в строку (в нижнем регистре)
    count = text_lower.count(char)

    # если больше 1 — выводим информацию о символе и количестве вхождений в строку
    if count > 1:
        print("Символ '" + char + "' встречается", count, "раз(а)")

    # добавляем в обработанные символы
    used = used + char
