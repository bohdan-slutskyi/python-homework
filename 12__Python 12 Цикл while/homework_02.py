""" 2. Палиндром

Напишите программу, которая
- запрашивает у пользователя целое число
и определяет, является ли оно палиндромом.

Число является палиндромом, если оно читается одинаково слева направо и справа налево.

ВНИМАНИЕ! Запрещено использовать функции реверса и слайсинг!

Пример вывода:
Введите число: 12321
Число 12321 является палиндромом.
Введите число: 1234
Число 1234 не является палиндромом.
"""
#  1 способ через полное реверсирование числа:

number = int(input("Введите целое положительное число: "))

if number < 0:
    print("Число", number, "не является палиндромом.") # отрицательные числа не являются палиндромами
elif number < 10:
    print("Число", number, "является палиндромом.") # однозначные числа являются палиндромами
else:
# Сохранить оригинальное число в отдельную переменную
    original_number = number
    reverse_number = 0
    while number > 0:
        last_digit = number % 10  #Получить последнюю цифру
        number = number // 10     #Убрать последнюю цифру
        reverse_number = reverse_number * 10 + last_digit #Добавить последнюю цифру в перевёрнутое число
    if reverse_number == original_number: #Проверить, равны ли оригинальное число и перевёрнутое число
        print("Число", original_number, "является палиндромом.")
    else:
        print("Число", original_number, "не является палиндромом.")

# 2 способ через половину реверсирования числа:

# 2. Проверить простые случаи:
#    - если число < 0 → не палиндром → завершить программу
#    - если число < 10 → палиндром → завершить программу

# 3. Создать переменную reversed_half = 0
#    (в ней будем собирать перевёрнутую правую половину числа)

# 4. Запустить цикл:
#    пока number > reversed_half
#    (это значит, что мы ещё не дошли до середины числа)

# 5. Внутри цикла выполнить 3 шага:
#    5.1 взять последнюю цифру числа:
#        last_digit = number % 10
#    5.2 добавить эту цифру в reversed_half:
#        reversed_half = reversed_half * 10 + last_digit
#    5.3 убрать последнюю цифру из number:
#        number = number // 10

# 6. Когда цикл завершился:
#    это значит, что мы дошли до середины числа

# 7. Выполнить проверку:
#    - если number == reversed_half → палиндром (для чётных чисел)
#    - если number == reversed_half // 10 → палиндром (для нечётных чисел,
