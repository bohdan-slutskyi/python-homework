""" 05.1 Добавление задач

Функция add_tasks()
- запрашивает задачи у пользователя
- и записывает их в файл tasks.txt.

Она работает в бесконечном цикле, пока пользователь не введёт exit.

Данные:
Файл tasks.txt – каждая строка содержит одно задание.

Пример вывода:
Введите задачу: Подготовить отчёт
Введите задачу: Провести собрание
Введите задачу: Проверить документацию
Введите задачу: Разработать новый модуль
Введите задачу: Настроить сервер
Введите задачу: exit

Пример файла:
Подготовить отчёт
Провести собрание
Проверить документацию
Разработать новый модуль
Настроить сервер

"""

def add_tasks():
    # Режим "a" — добавляем строки в конец файла, не удаляя существующие
    with open("tasks.txt", "a", encoding="utf-8") as f:
        while True:
            task = input("Введите задачу: ")
            if task.lower() == "exit":
                break
            f.write(task + "\n")
            f.flush()  # flush гарантирует, что данные сразу попали в файл (без буферизации)


if __name__ == "__main__":
    add_tasks()


