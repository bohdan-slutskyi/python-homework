""" 03. Рамка вокруг вывода

Создайте декоратор frame, который
- оборачивает результат функции рамкой из 50 символов -,
     выводя по строке до и после вызова функции.

Пример декорируемой функции:
def say_hello():
    print("Привет, игрок!")
Пример вывода:
--------------------------------------------------
Привет, игрок!
--------------------------------------------------
"""

from typing import Callable


# Источник: ../../python-course/62__Python 62 Вложенные функции. Замыкание/materials/theory_02__Enclosing_scope.md — вложенная функция получает доступ к func
def frame(func: Callable[[], None]) -> Callable[[], None]:
    """Добавить строки рамки до и после вызова функции."""

    def wrapper() -> None:
        print("-" * 50)
        func()
        print("-" * 50)

    return wrapper


@frame
def say_hello() -> None:
    print("Привет, игрок!")


say_hello()
