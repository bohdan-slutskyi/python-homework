""" 03 Объединение словарей

Напишите функцию, которая
- принимает любое количество словарей
- и объединяет их в один.

Если ключи повторяются, используется значение из последнего словаря.

Данные:
dict1 = {"a": 1, "b": 2}
dict2 = {"b": 3, "c": 4}
dict3 = {"d": 5}

Пример вызова:
print(merge_dicts(dict1, dict2, dict3))
{'a': 1, 'b': 3, 'c': 4, 'd': 5}
"""


def merge_dicts(*dicts):
    # Здесь будет храниться итоговый объединённый словарь.
    result = {}

    # Перебираем все словари, переданные в функцию.
    for current_dict in dicts:
        # Перебираем ключи текущего словаря.
        for key in current_dict:
            # Если ключ уже существует, новое значение перезапишет старое.
            result[key] = current_dict[key]

    # Возвращаем готовый объединённый словарь.
    return result


dict1 = {"a": 1, "b": 2}
dict2 = {"b": 3, "c": 4}
dict3 = {"d": 5}

print(merge_dicts(dict1, dict2, dict3))
# {'a': 1, 'b': 3, 'c': 4, 'd': 5}
