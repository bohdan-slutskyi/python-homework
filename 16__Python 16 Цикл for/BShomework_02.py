""" 02 Лестница чисел

Напишите программу, которая
- принимает число n
- и выводит "лестницу" из чисел.

Каждая строка лестницы содержит числа от 1 до номера строки.
"""

# просим пользователя ввести число и превращаем его в целое число
n = int(input("Введите число: "))

# идём по строкам от 1 до n
for i in range(1, n + 1):

    # внутри каждой строки выводим числа от 1 до i
    for j in range(1, i + 1):

        # печатаем текущее число без перехода на новую строку
        print(j, end=" ")

    # после завершения строки переходим на новую строку
    print()
