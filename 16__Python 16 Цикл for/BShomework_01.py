""" 01 Таблица умножения

Напишите программу, которая выводит таблицу умножения для чисел от 1 до n.
Где n это число, которое введет пользователь.
Оформите вывод так, чтобы столбцы были ровные (число ровно под числом)

ВНИМАНИЕ: решать БЕЗ использования встроенных методов форматирования строк!
"""
# Таблица умножения для чисел от 1 до n выглядит так:
"""""
      j=1  j=2  j=3  j=4  j=5
i=1    1    2    3    4    5
i=2    2    4    6    8   10
i=3    3    6    9   12   15
i=4    4    8   12   16   20
i=5    5   10   15   20   25
"""
# просим пользователя ввести число и сразу превращаем ввод в целое число
# n = int(input("Введите число: "))
n = 5
# самое большое число в таблице — это n умножить на n
max_number = n * n

# превращаем самое большое число в строку и узнаём, сколько цифр в этом числе
cell_width = len(str(max_number))

# идём по строкам таблицы: 1, 2, 3, ..., n
for i in range(1, n + 1):
    # внутри каждой строки идём по столбцам: 1, 2, 3, ..., n
    for j in range(1, n + 1):
        # считаем результат умножения номера строки на номер столбца
        result = i * j

        # превращаем результат в строку
        result_str = str(result)

        # узнаём, сколько цифр в результате
        result_length = len(result_str)

        # считаем, сколько пробелов не хватает слева,
        # чтобы число заняло столько же места, сколько самое длинное число
        spaces = cell_width - result_length

        # печатаем:
        # 1) нужное количество пробелов слева
        # 2) само число
        # 3) один пробел после числа
        # и не переходим на новую строку
        print(" " * spaces + result_str, end=" ")

    # когда вся строка таблицы напечатана, переходим на новую строку
    print()
