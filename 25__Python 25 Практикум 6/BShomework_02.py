""" 02 Повторяющиеся элементы

Напишите программу, которая
- находит индексы элементов тюпла, встречающихся более одного раза.
- Вывести сами элементы и их индексы.

Данные:
numbers = (1, 2, 3, 4, 2, 5, 3, 6, 4, 2, 9)
Пример вывода:
Индексы элемента 2: 	1 4 9
Индексы элемента 3: 	2 6
Индексы элемента 4: 	3 8
"""

numbers = (1, 2, 3, 4, 2, 5, 3, 6, 4, 2, 9)
used = []  # сюда будем записывать уже обработанные элементы
for i, num in enumerate(numbers):
    # если элемент уже обрабатывали — пропускаем
    if num in used:
        continue
    indexes = []  # сюда собираем индексы текущего элемента
    # ищем все индексы этого элемента
    for j, value in enumerate(numbers):
        if value == num:
            # добавляем индекс (через +, как в теории)
            indexes = indexes + [j]
    # если элемент встречается больше одного раза
    if len(indexes) > 1:
        print("Индексы элемента", num, ":\t", end="")
        # выводим индексы
        for idx in indexes:
            print(idx, end=" ")
        print()
    # помечаем элемент как обработанный
    used = used + [num]



