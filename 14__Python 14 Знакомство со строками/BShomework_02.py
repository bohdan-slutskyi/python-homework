"""02 Подстрока с заполнением

Напишите программу, которая
- принимает строку
- и индексы начала и конца.
Программа должна вывести подстроку на указанных позициях.
Также если конечный индекс выходит за пределы строки,
программа заполняет недостающие символы символами _.

Пример вывода:
Введите строку: Программирование
Введите начальный индекс: 3
Введите конечный индекс: 20
Подстрока: граммирование_____
"""

# Получаем ввод от пользователя
string = input("Введите строку: ")
start_index = int(input("Введите начальный индекс: "))
end_index = int(input("Введите конечный индекс: "))

# Берём подстроку (Python сам обрежет, если индекс большой)
substring = string[start_index:end_index]

# Считаем, сколько символов не хватает для достижения end_index
missing = end_index - start_index - len(substring)

# Добавляем недостающие "_" к подстроке
substring = substring + "_" * missing

# Вывод результата
print("Подстрока:", substring)
