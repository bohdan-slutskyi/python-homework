""" 03 Удаление всех повторяющихся символов

Напишите программу, которая
- принимает строку
- и удаляет из неё все повторяющиеся символы, сохраняя только первые их вхождения.
"""

# просим пользователя ввести строку
# text = input("Введите строку: ")
text = "Python programming"
# создаём пустую строку, в которую будем собирать результат
result = ""

# идём по каждому символу исходной строки
for character in text:

    # проверяем: если такого символа ещё нет в результате
    if character not in result:

        # добавляем символ в результат (сохраняем первое вхождение)
        result = result + character

# выводим итоговую строку без повторяющихся символов
print("Результат:", result)
