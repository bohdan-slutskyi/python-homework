""" 01 Не уникальные числа

Напишите программу, которая
- находит все числа, встречающиеся более одного раза в списке,
- и выводит их в порядке убывания.

Данные:
numbers = [4, 7, 3, 7, 8, 3, 4, 2, 7, 3, 8, 4]
Пример вывода:
Числа, встречающиеся более одного раза: [8, 7, 4, 3]
"""

numbers = [4, 7, 3, 7, 8, 3, 4, 2, 7, 3, 8, 4]

# словарь для подсчёта количества чисел в списке
counts = {}
for n in numbers:
    counts[n] = counts.get(n, 0) + 1

# находим числа, встречающиеся более одного раза и сохраняем их в список
unique_numbers = [n for n, count in counts.items() if count > 1]

# выводим числа в порядке убывания (сортируем список в обратном порядке)
print("Числа, встречающиеся более одного раза:", sorted(unique_numbers, reverse=True))